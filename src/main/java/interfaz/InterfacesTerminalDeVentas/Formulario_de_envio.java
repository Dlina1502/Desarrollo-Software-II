    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        System.out.print("Registrando datos");
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        String fecha = dtf.format(LocalDateTime.now());
        
        String documentoR = jTextField50.getText();
        String nombreR = jTextField51.getText();
        String apellidoR = jTextField52.getText();
        String telefonoR = jTextField59.getText();
        String direccionR = jTextField63.getText();
        String sede = (String) jComboBox4.getSelectedItem();
        String ciudadS = jTextField56.getText();
        String[] partsSede = sede.split("//");
        funciones.registrarRemitente(documentoR, nombreR, apellidoR, telefonoR, direccionR+"//"+ciudadS, partsSede[2]);
        String documentoD = jTextField55.getText();
        String nombreD = jTextField54.getText();
        String apellidoD = jTextField53.getText();
        String telefonoD = jTextField60.getText();
        String direccionD = jTextField64.getText();
        String ciudadL = jTextField57.getText();
        funciones.registrarDestinatario(documentoD, nombreD, apellidoD, telefonoD, direccionD+"//"+ciudadL, partsSede[2]);  
        String cantidad = jTextField61.getText();
        String descripcion = jTextField1.getText();
        String preciopaquete = jTextField2.getText();
        String tiposeguro = (String) jComboBox16.getSelectedItem();
        funciones.registrarEnvio(documentoR, documentoD, preciopaquete, fecha, descripcion, cantidad, tiposeguro);
        String tipopago = (String) jComboBox3.getSelectedItem();
        String precioenvio = jTextField62.getText();
        funciones.registrarFactura(partsSede[0], partsSede[1], partsSede[2], tipopago, fecha, precioenvio);
        JOptionPane.showMessageDialog(this,"Factura creada");
        
        funciones.generarFactura();
        funciones.abrirFactura();
        
    }//GEN-LAST:event_jButton1MouseClicked

